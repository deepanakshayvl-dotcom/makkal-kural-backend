# Get all 234 constituencies as a flat list
ALL_CONSTITUENCIES = []
for district, constituencies in TAMIL_NADU_CONSTITUENCIES.items():
    for constituency in constituencies:
        ALL_CONSTITUENCIES.append({
            "name": constituency,
            "district": district
        })

# Helper function to get constituencies by district
def get_constituencies_by_district(district):
    return TAMIL_NADU_CONSTITUENCIES.get(district, [])

# Helper function to get district by constituency
def get_district_by_constituency(constituency_name):
    for district, constituencies in TAMIL_NADU_CONSTITUENCIES.items():
        if constituency_name in constituencies:
            return district
    return None
